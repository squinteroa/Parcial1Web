   //Aqui se suma 
class Calculadora{
    sumar(num1,num2){
        return num1 + num2;
    }
   // Aqui se hacen las restas 
    restar(num1,num2){
        return num1 - num2;
    }
  // Aqui se divide
    dividir(num1,num2){
        return num1 / num2; 
    }
   // Aqui se multiplica
    multiplicar(num1,num2){
        return num1 * num2;
    }
}